	AREA RESET,DATA,READONLY
	EXPORT __Vectors
	
__Vectors
	DCD 0X10001000;		S.P. INIT
	DCD Reset_Handler

	AREA MYC,CODE,READONLY
	ENTRY
	EXPORT  Reset_Handler
	
Reset_Handler

	LDR R13,=0x10001000
	MOV R0,#val	
	MOV R1,#1
	LDR R2,=res
	MOV R3,#1		;STORES THE FINAL RESULT
	
	BL fi
	
	STR R3,[R2]
BK	B BK

fi	CMP R0,#1
	BEQ dn
	PUSH {R0}	;GOTTA STORE THAT N
	SUB R0,R0,#1
	PUSH {LR}	; GOTTA STORE THAT BRANCH LOCATION
	BL fi
	POP {LR}
	POP {R4}
	MUL R3,R3,R4
dn
	BX LR

val	EQU 4
	AREA MYD,DATA,READWRITE
res	DCD 0 
	END