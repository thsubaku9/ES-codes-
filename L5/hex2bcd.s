Reset_Handler
	LDR R0,=HEX
	LDR R2,=BCD
	LDR R1,[R0]
	MOV R8,#0
BACK
	BL DIVIDE
	CMP R1,#0
	BNE BACK
	;LSR R8,R7
	STR R8,[R2]
STOP B STOP

DIVIDE
	MOV R3,#0
UP 
	CMP R1,#0X0A
	BLO DOWN
	SUB R1,#0X0A
	ADD R3,#1
	B UP
DOWN 
    ORR R8,R1
	ROR R8,#4
	MOV R1,R3
	BX LR
	
HEX DCD 0xFE
	AREA mydata,DATA,READWRITE
