;BCD2HEX
	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0X10001000
	ALIGN
	DCD Reset_Handler
	AREA MYC,CODE,READONLY
	ENTRY 
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=N1
	LDR R0,[R0];LOAD NUMBER
	MOV R1,#0;SUMMER
	MOV R2,#0;COUNTER
	MOV R4,#1
	MOV R5,#0X0A

L1	CMP R2,#8
	BHS DN
	MOV R3,#0X0F
	AND R3,R0
	MLA R1,R3,R4,R1
	MUL R4,R5
	ROR R0,#4
	ADD R2,#1
	B L1
	
DN	LDR R6,=RES
	STR R1,[R6]
BK	B BK

N1 DCD 0X123456
	AREA MYD,DATA,READWRITE
RES DCD 0
	END