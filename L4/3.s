	AREA RESET, DATA, READONLY
	EXPORT __Vectors

__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler

Reset_Handler

		LDR R0,=NUM	
		LDR R3,=RESULT
		MOV R5,#4
LOOP	LDR R1,[R0]
		AND R2,R1,#0x0000000F
		CMP R2,#09
		BLO DOWN

DOWN	STR R2,[R3]
		AND R4,R1,#0xFFFFFFF0
		MOV R4,R4,LSR#04
		CMP R4,#09
		BLO DOWN1
		
DOWN1	STR R4,[R3,#01]
		
		SUB R5,#1
		TEQ R5,#0
		BNE LOOP
		
BK	B BK		
NUM DCD 0x30D39B3A
		AREA mydata,DATA,READWRITE	
RESULT DCD 0
	END