	AREA RESET,DATA,READONLY
	EXPORT __Vectors
	
__Vectors
	DCD 0X10001000
	DCD Reset_Handler
	
	AREA myc,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler

Reset_Handler
;GCD FINDER
	LDR R1,=N1
	LDR R2,=N2
	LDR R3,[R0]
	LDR R4,[R1]
	LDR R8,=GCD
	
BK	
	CMP R4,#0
	BEQ DONE
	
	CMP R3,R4
	BLO ST
	LDR R6,R3
	MOV R3,R4
	MOV R4,R6
	
	
	
ST	MOV R5,#1
PER	MUL R7,R3,R5
	CMP R7,R4
	BLO I
	BGE II
I	ADD R5,#1
	B PER
II	SUB R5,R5,#1
	MUL R7,R3,R5
	SUB R4,R4,R7
	B BK
	
DONE
	STR R3,[R8]

KB	B KB			
	
N1 DCD 0X2A
N2 DCD 0X07
	AREA myd,DATA,READWRITE
GCD DCD 0
	END